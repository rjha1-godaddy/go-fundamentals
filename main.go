package main

import "fmt"

func main() {
	// Variable declaration
	var name string = "Alice"
	var age int = 18
	var height float64 = 5.1
	var isDeveloper bool = false

	// Type inference
	city := "Wonderland"

	// Constants
	const monarch = "Red Queen"

	fmt.Println("Name:", name)
	fmt.Println("Age:", age)
	fmt.Println("Height:", height)
	fmt.Println("Is Developer?", isDeveloper)
	fmt.Println("City:", city)
	fmt.Println("Monarch:", monarch)
}
